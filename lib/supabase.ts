import { cookies } from "next/headers";
import {
  createServerClient,
  createBrowserClient as createBrowserSupabaseClient,
} from "@supabase/ssr";
import type { SupabaseClient } from "@supabase/supabase-js";

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL!;
const SUPABASE_ANON_KEY = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!;

// Server-side client (for Server Components & Server Actions)
export async function createClient() {
  const cookieStore = await cookies();
  return createServerClient(SUPABASE_URL, SUPABASE_ANON_KEY, {
    cookies: {
      getAll() {
        return cookieStore.getAll();
      },
      setAll(cookiesToSet) {
        cookiesToSet.forEach(({ name, value, options }) =>
          cookieStore.set(name, value, options),
        );
      },
    },
  });
}

// Client-side client (for Client Components)
export function createBrowserClient(): SupabaseClient {
  return createBrowserSupabaseClient(SUPABASE_URL, SUPABASE_ANON_KEY);
}
